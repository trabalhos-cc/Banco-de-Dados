package bensDireito;

public class BensDireito {

	private int idBensDireito;
	private String descriminacao;
	private float situacaoFisrt;
	private float situacaoLast;
	private int item;
	private String nome;
	private int documento;
	private int idPais;
	
	public int getIdBensDireito() {
		return idBensDireito;
	}
	public void setIdBensDireito(int idBensDireito) {
		this.idBensDireito = idBensDireito;
	}
	public String getDescriminacao() {
		return descriminacao;
	}
	public void setDescriminacao(String descriminacao) {
		this.descriminacao = descriminacao;
	}
	public float getSituacaoFisrt() {
		return situacaoFisrt;
	}
	public void setSituacaoFisrt(float situacaoFisrt) {
		this.situacaoFisrt = situacaoFisrt;
	}
	public float getSituacaoLast() {
		return situacaoLast;
	}
	public void setSituacaoLast(float situacaoLast) {
		this.situacaoLast = situacaoLast;
	}
	public int getItem() {
		return item;
	}
	public void setItem(int item) {
		this.item = item;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getDocumento() {
		return documento;
	}
	public void setDocumento(int documento) {
		this.documento = documento;
	}
	public int getIdPais() {
		return idPais;
	}
	public void setIdPais(int idPais) {
		this.idPais = idPais;
	}
	
	
	
}
