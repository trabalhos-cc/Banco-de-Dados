package uf;

public class Uf {

	private int idUF;
	private String nome;
	private int idPais;
	
	public int getIdUF() {
		return idUF;
	}
	public void setIdUF(int idUF) {
		this.idUF = idUF;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIdPais() {
		return idPais;
	}
	public void setIdPais(int idPais) {
		this.idPais = idPais;
	}
	
	
}
