package enderecoExterior;

public class EnderecoExterior {

	private int idEnderecoExterior;
	private int CPFProcurador;
	private int codigoPostal;
	private int idEmbaixada;
	
	public int getIdEnderecoExterior() {
		return idEnderecoExterior;
	}
	public void setIdEnderecoExterior(int idEnderecoExterior) {
		this.idEnderecoExterior = idEnderecoExterior;
	}
	public int getCPFProcurador() {
		return CPFProcurador;
	}
	public void setCPFProcurador(int cPFProcurador) {
		CPFProcurador = cPFProcurador;
	}
	public int getCodigoPostal() {
		return codigoPostal;
	}
	public void setCodigoPostal(int codigoPostal) {
		this.codigoPostal = codigoPostal;
	}
	public int getIdEmbaixada() {
		return idEmbaixada;
	}
	public void setIdEmbaixada(int idEmbaixada) {
		this.idEmbaixada = idEmbaixada;
	}
	
	
}
