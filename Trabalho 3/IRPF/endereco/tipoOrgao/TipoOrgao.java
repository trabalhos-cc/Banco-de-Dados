package tipoOrgao;

public class TipoOrgao {

	private int idTipoOrgao;
	private String nome;
	
	public int getIdTipoOrgao() {
		return idTipoOrgao;
	}
	public void setIdTipoOrgao(int idTipoOrgao) {
		this.idTipoOrgao = idTipoOrgao;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
}
