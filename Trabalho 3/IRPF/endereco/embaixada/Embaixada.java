package embaixada;

public class Embaixada {

	private int idEmbaixada;
	private int idCidade;
	private int idPais;
	private int idTipoOrgao;
	
	
	public int getIdEmbaixada() {
		return idEmbaixada;
	}
	public void setIdEmbaixada(int idEmbaixada) {
		this.idEmbaixada = idEmbaixada;
	}
	public int getIdCidade() {
		return idCidade;
	}
	public void setIdCidade(int idCidade) {
		this.idCidade = idCidade;
	}
	public int getIdPais() {
		return idPais;
	}
	public void setIdPais(int idPais) {
		this.idPais = idPais;
	}
	public int getIdTipoOrgao() {
		return idTipoOrgao;
	}
	public void setIdTipoOrgao(int idTipoOrgao) {
		this.idTipoOrgao = idTipoOrgao;
	}
	
	
	
}
