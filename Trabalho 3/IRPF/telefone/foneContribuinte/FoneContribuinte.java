package foneContribuinte;

public class FoneContribuinte {

	private int idFoneContribuinte;
	private int idContribuinte;
	private int idDdd;
	private int idDdi;
	private int idTipoFone;
	private int numero;
	
	
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}
	public int getIdFoneContribuinte() {
		return idFoneContribuinte;
	}
	public void setIdFoneContribuinte(int idFoneContribuinte) {
		this.idFoneContribuinte = idFoneContribuinte;
	}
	public int getIdContribuinte() {
		return idContribuinte;
	}
	public void setIdContribuinte(int idContribuinte) {
		this.idContribuinte = idContribuinte;
	}
	public int getIdDdd() {
		return idDdd;
	}
	public void setIdDdd(int idDdd) {
		this.idDdd = idDdd;
	}
	public int getIdDdi() {
		return idDdi;
	}
	public void setIdDdi(int idDdi) {
		this.idDdi = idDdi;
	}
	public int getIdTipoFone() {
		return idTipoFone;
	}
	public void setIdTipoFone(int idTipoFone) {
		this.idTipoFone = idTipoFone;
	}
	
	
}
