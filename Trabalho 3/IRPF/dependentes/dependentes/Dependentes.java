package dependentes;

public class Dependentes {

	private int idDependentes;
	private int CPF;
	private String Dt;
	private String nome;
	private float valor;
	private int item;
	private int idTipodependente;
	public int getIdDependentes() {
		return idDependentes;
	}
	public void setIdDependentes(int idDependentes) {
		this.idDependentes = idDependentes;
	}
	public int getCPF() {
		return CPF;
	}
	public void setCPF(int cPF) {
		CPF = cPF;
	}
	public String getDt() {
		return Dt;
	}
	public void setDt(String dt) {
		Dt = dt;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public float getValor() {
		return valor;
	}
	public void setValor(float valor) {
		this.valor = valor;
	}
	public int getItem() {
		return item;
	}
	public void setItem(int item) {
		this.item = item;
	}
	public int getIdTipodependente() {
		return idTipodependente;
	}
	public void setIdTipodependente(int idTipodependente) {
		this.idTipodependente = idTipodependente;
	}
	
	
}
