package ocupacao;

public class Ocupacao {

	private int idOcupacao;
	private int idNatureza;
	private int idOcupacaoPrincipal;
	
	public int getIdOcupacao() {
		return idOcupacao;
	}
	public void setIdOcupacao(int idOcupacao) {
		this.idOcupacao = idOcupacao;
	}
	public int getIdNatureza() {
		return idNatureza;
	}
	public void setIdNatureza(int idNatureza) {
		this.idNatureza = idNatureza;
	}
	public int getIdOcupacaoPrincipal() {
		return idOcupacaoPrincipal;
	}
	public void setIdOcupacaoPrincipal(int idOcupacaoPrincipal) {
		this.idOcupacaoPrincipal = idOcupacaoPrincipal;
	}
	
	
}
