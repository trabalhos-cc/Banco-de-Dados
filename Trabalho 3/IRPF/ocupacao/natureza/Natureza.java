package natureza;

public class Natureza {

	private int idNatureza;
	private String nome;
	
	public int getIdNatureza() {
		return idNatureza;
	}
	public void setIdNatureza(int idNatureza) {
		this.idNatureza = idNatureza;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
}
